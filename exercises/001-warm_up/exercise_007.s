; ---- CODE ------------------------------------------------------------------------
.segment "CODE"
.org        $8000

Reset:      
            cld

            lda #10
            sta $80

            inc $80
            dec $80

NMI:
            rti

IRQ:
            rti
; ----------------------------------------------------------------------------------

