; ---- INES HEADER -----------------------------------------------------------------
.segment "HEADER"
.org        $7FF0
.byte       $4E,$45,$53,$1A,$02,$01,$00,$00,$00,$00,$00,$00,$00,$00,$00,$00
; ----------------------------------------------------------------------------------

; ---- CODE ------------------------------------------------------------------------
.segment "CODE"
.org        $8000

Reset:      
            cld

            lda #$A
            ldx #%1010

            sta $80
            stx $81

            lda #10
            clc
            adc $80
            clc
            adc $81

            sta $82

NMI:
            rti

IRQ:
            rti
; ----------------------------------------------------------------------------------

; ---- VECTOR ----------------------------------------------------------------------
.segment "VECTORS"
.org        $FFFA
.word       NMI
.word       Reset
.word       IRQ
; ----------------------------------------------------------------------------------